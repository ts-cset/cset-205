CREATE DATABASE `flightdata`;
USE `flightdata`;

CREATE TABLE `airports` (
  `airport` varchar(3) NOT NULL,
  `latitude` varchar(255) NOT NULL,
  `longitude` varchar(255) NOT NULL
);
CREATE TABLE `flights` (
  `year` int(11) NOT NULL,
  `month` int(11) NOT NULL,
  `dayofmonth` int(11) NOT NULL,
  `dayofweek` int(11) NOT NULL,
  `deptime` varchar(255) NOT NULL,
  `arrtime` varchar(255) NOT NULL,
  `uniquecarrier` varchar(255) NOT NULL,
  `flightnum` int(11) NOT NULL,
  `tailnum` varchar(255) NOT NULL,
  `airtime` int(11) NOT NULL,
  `arrdelay` int(11) NOT NULL,
  `depdelay` int(11) NOT NULL,
  `origin` varchar(255) NOT NULL,
  `dest` varchar(255) NOT NULL,
  `distance` int(11) NOT NULL
);

